import math # Funções matemáticas básicas 
numero = 16 
print("Númeero:", numero) 
print("Raiz quadrada:",math.sqrt(numero)) 
print("Logaritmo natural:", math.log(numero)) 
print("Logaritmo base 10:", math.log10(numero)) # Funções trigonométricas 
angulo = math.pi / 4 # 45 graus em radianos 
print("Ângulo:", angulo)
print("Seno:",math.sin(angulo)) 
print("Cosseno:", math.cos(angulo))
print("Tangente:", math.tan(angulo)) # Constantes matemáticas 
print("Pi:", math.pi) 
print("Euler:", math.e) #Funções de arredondamento 
numero_decimal = 3.7
print("Número:", numero_decimal) 
print("Arredondar para cima:", math.ceil(numero_decimal)) 
print("Arredondar para baixo:", math.floor(numero_decimal)) 
print("Valor absoluto:", abs(-5))
